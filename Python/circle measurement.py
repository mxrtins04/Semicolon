"""
- Assign the value 3.14159 to the variable pie
- Assign the value 2 to the varable radius
- Initialize the variable, diameter, to 2 multiplied by radius
- Initialize the variable, circumference, to 2 multiplied by pie multiplied by radius
- Initialize the variable, area, to pie multiplied by the square of radius
- Display diameter, circumference and area
"""



pie = 3.14159
radius = 2
diameter = 2 * radius
circumference = 2 * pie * radius
area = pie * radius ** 2

Print(diameter)
print(circumference)
print(area)
